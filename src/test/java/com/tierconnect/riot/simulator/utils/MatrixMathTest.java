    @Test
    public void testCreateTransitionVector(){
        RealMatrix result = MatrixMath.convertMatrixReal(matrix);
        result = MatrixMath.createTransitionVector(result);
        for(int i = 0; i < result.getRowDimension(); i++){
            assertEquals(Math.round(DoubleStream.of(result.getRow(i)).sum()), 1);
        }
    }

