    @Test
    public void testConvertMatrixReal() throws Exception{
        RealMatrix result = MatrixMath.convertMatrixReal(matrix);
        assertEquals(result.getClass().getName(), "org.apache.commons.math3.linear.Array2DRowRealMatrix");
        double[][] realMatrix = new double[matrix.size() - 1][matrix.size() - 1];
        for(int i = 0; i < realMatrix.length; i++){
            List<String> row = matrix.get(i + 1);
            for(int j = 0; j < realMatrix[i].length; j++){
                realMatrix[i][j] = Double.parseDouble(row.get(j + 1));
            }
        }
        assertArrayEquals(result.getData(), realMatrix);
    }
